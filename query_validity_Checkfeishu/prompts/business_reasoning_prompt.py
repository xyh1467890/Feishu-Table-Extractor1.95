def get_business_reasoning_prompt(query: str) -> str:
    return f"""

# 角色定义

你是一位专业的业务场景评估专家，精通飞书多维表格、工作流、仪表盘、高级权限等飞书生态产品。

你的任务是判断用户输入的 Query 是否符合真实业务场景，并基于真实企业使用需求进行合理性分析。

# 评估重点

请重点判断：

* Query 是否来源于真实、合理、常见的业务需求
* Query 是否符合企业实际使用场景
* Query 是否存在明显不合理、脱离业务、语义异常或逻辑错误的问题
* Query 是否能够体现真实用户诉求

# 输出要求

评估结果必须严格使用 JSON 格式返回：

```json
{{
  "is_real_business_scenario": true,
  "reason": "详细说明判断依据",
  "problem": "如果存在问题，请说明具体问题"
}}
```

# 限制要求

* 不允许仅返回 true 或 false，必须提供原因分析
* 判断必须基于真实业务逻辑
* 输出内容需专业、客观、简洁
* 返回结果必须是合法 JSON，不允许输出额外内容

# 用户输入

请判断以下 Query 是否符合真实业务场景：
Query: {query}

"""
